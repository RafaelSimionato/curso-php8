# Uma classe é um molde/forma/modelo a partir do qual criamos objetos
# Exemplo: a classe Humano é um modelo para, a partir dela, criarmos um,
# conjunto de homens e mulheres, cada um partilhando o mesmo "molde"
# mas com propriedades e funções com valores diferentes
