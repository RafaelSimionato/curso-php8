# e a mulher cabelo claro

# Teoricamente:
# Class Humano
#   > cabelo          // propriedade
#   > genero          // propriedade
#   > peso            // propriedade
#   > caminhar        // metodo

# Homem -> Humano           //Lê-se: Homem é o objeto criado a partir da classe Humano
#   > cabelo - castanho
#   > genero - masculino
#   > peso - 75
#   > caminhar()

# Mulher -> Humano
#   > cabelo - claro
#   > genero - feminino
